
//mouseover

const funBusTitle = document.querySelector('.logo-heading');
funBusTitle.addEventListener(
    "mouseover",
        event => {
            event.target.style.color = "red";
        setTimeout(function() {
            event.target.style.color = "";
        }, 500);
    },
    false
    );

