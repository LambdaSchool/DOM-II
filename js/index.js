// 1st 2 unique event listeners- "mouseenter" & "mouseleave"
 const navlink = document.querySelectorAll("a").forEach(navlink =>{navlink.addEventListener("mouseenter", () =>{
    navlink.style.color = "hotpink";
    navlink.style.backgroundColor = "gray";
    navlink.style.padding= "3%";
 })
    navlink.addEventListener("mouseleave", ()=>{
        navlink.style.color = "black";
        navlink.style.backgroundColor = "white";
      })
})

